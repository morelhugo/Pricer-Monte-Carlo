///////////////////////////////////////////////////////////////////////////

double CPayOff::d1(double T, double r, double sigma, double S0, double K) const
{
    return ((log(S0 / K) + (r + 0.5 * pow(sigma, 2) * T)) / (sigma * sqrt(T))) ;
}


///////////////////////////////////////////////////////////////////////////

double CPayOff::d2(double T, double r, double sigma, double S0, double K) const
{
     return ((log(S0 / K) + (r - 0.5 * pow(sigma, 2) * T)) / (sigma * sqrt(T)));
}

