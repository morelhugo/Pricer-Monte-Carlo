#include <stdio.h>
#include <cstdlib>
#include <cmath>

///////////////////////////////////////////////////////////////////////////

#include "CBrownian.hpp"

///////////////////////////////////////////////////////////////////////////

CBrownian::CBrownian(double Ti, long Ni) : CProcess(Ti, Ni)
{
    No = CNormal(0, 1);
}

///////////////////////////////////////////////////////////////////////////

CBrownian::CBrownian(double Ti, double Dti) : CProcess(Ti, Dti)
{
    No = CNormal(0, 1);
}

///////////////////////////////////////////////////////////////////////////

CBrownian::~CBrownian()
{
}

///////////////////////////////////////////////////////////////////////////

double CBrownian::getIncrementProcessT() const
{
    return sqrt(T) * No.simule() ;
}


///////////////////////////////////////////////////////////////////////////

double CBrownian::getIncrementProcessDt() const
{
    return sqrt(Dt) * No.simule() ;
}

