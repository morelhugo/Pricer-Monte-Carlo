#include "CDiscountFactor.hpp"

///////////////////////////////////////////////////////////////////////////

#include <cmath>

///////////////////////////////////////////////////////////////////////////

CDiscountFactor::CDiscountFactor(double ri)
{
	r = ri ;
}

///////////////////////////////////////////////////////////////////////////

CDiscountFactor::~CDiscountFactor()
{
}

///////////////////////////////////////////////////////////////////////////

double CDiscountFactor::eval(double T) const
{
	return exp(- r * T) ;
}

