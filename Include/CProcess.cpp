#include <stdio.h>
#include <cstdlib>
#include <cmath>

///////////////////////////////////////////////////////////////////////////

#include "CProcess.hpp"

///////////////////////////////////////////////////////////////////////////

CProcess::CProcess(double Ti, long Ni) 
{
    T  = Ti ;
    N  = Ni ;
    Dt = T / double(N) ;
}

///////////////////////////////////////////////////////////////////////////

CProcess::CProcess(double Ti, double Dti)
{
    T  = Ti ;
    Dt = Dti ;
    N  = long(T / Dt) ;
}

///////////////////////////////////////////////////////////////////////////

CProcess::~CProcess()
{
}

///////////////////////////////////////////////////////////////////////////

double CProcess::simule() const
{
    return getIncrementProcessT();
}

